    public void populateTable() {

    this.users = DatabaseConnector.getUsers();
        
        DefaultTableModel model = (DefaultTableModel) this.userjTable.getModel();
        model.setRowCount(0);
        for (User u1: users){
            Object [] row = new Object[9];
            row[0] = u1.getUserID();
            row[1] = u1.getfName();
            row[2] = u1.getlName();
            row[3] = u1.getGender();
            row[4] = u1.getAge();
            row[5] = u1.getPhoneNumber();
            row[6] = u1.getEmail();
            row[7] = u1.getCollege();
            row[8] = u1.getHobby();
            model.addRow(row);
        }

    }
    
