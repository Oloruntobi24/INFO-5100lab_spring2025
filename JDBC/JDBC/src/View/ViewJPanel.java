    private void submitEditjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitEditjButtonActionPerformed
        // TODO add your handling code here:
        User updatedUser = new User();
        updatedUser.setfName(this.firstNameTextField.getText());
        updatedUser.setlName(this.lastNameTextField.getText());
        updatedUser.setGender( this.genderComboBox.getSelectedItem().toString());
        updatedUser.setAge(Integer.valueOf(this.ageTextField.getText()));
        updatedUser.setEmail(this.emailTextField.getText());
        updatedUser.setPhoneNumber(this.phoneNumberTextField.getText());
        updatedUser.setCollege(this.collegeComboBox.getSelectedItem().toString());
        updatedUser.setHobby(this.hobbyTextArea.getText());
        
        
        try {
            DatabaseConnector.updateUser(user, updatedUser);
            System.out.println("record updated");
            
            populateTable();
            
            JOptionPane.showMessageDialog(this,"Information updated successfully", "Success!", JOptionPane.INFORMATION_MESSAGE);

        } catch(Exception e){
            JOptionPane.showMessageDialog(this, "Please fill in boxes.", "Oops!", JOptionPane.ERROR_MESSAGE);   

            
        }
        
    }//GEN-LAST:event_submitEditjButtonActionPerformed


