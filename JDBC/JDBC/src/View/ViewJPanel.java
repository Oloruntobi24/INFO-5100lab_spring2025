    private void deletejButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletejButtonActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = this.userjTable.getSelectedRow();
        
        try{
            this.user = this.users.get(selectedRowIndex);
        
        DatabaseConnector.deleteUser(user);
        System.out.println("User deleted successfully");
        
        JOptionPane.showMessageDialog(this,"Information deleted successfully", "Success!", JOptionPane.INFORMATION_MESSAGE);
        
        populateTable();
            
        }catch(Exception e) {
             JOptionPane.showMessageDialog(this, "Please select an input.", "Oops!", JOptionPane.ERROR_MESSAGE);
        }
        
        
    }//GEN-LAST:event_deletejButtonActionPerformed

